package com.array.pro;

public class ArrayProgram {
	public static void main(String[] args) {
		Float[] e=new Float[5];
		e[0]=(float) 1.5;
		e[1]=(float) 2.5;
		e[2]=(float) 3.5;
		e[3]=(float) 4.2;
		e[4]=(float) 1.3;
	
		
		System.out.println("the size is = " + e.length);
		
		System.out.println("the value of 0 ="+e[0]);
		System.out.println("the value of 1="+e[1]);
		System.out.println("the value of 2="+e[2]);
		System.out.println("the value of 3="+e[3]);
		System.out.println("the value of 4 ="+e[4]);
		
	}

}
