package single.inheritance.pro;

public class parent {
	public void food() {
		System.out.println("briyani food");

	}
	public void dress() {
	System.out.println("white dress");

	}
	public void property() {
	System.out.println("property");

	}
	
	public static void main(String[] args) {
		parent b=new parent();
		b.food();
		b.dress();
		b.property();
	}

}
